table = {
    'q0': {'1': ['1', 1, 'q1'], '2': ['2', 1, 'q1'], ' ': [' ', 1, 'q0']},
    'q1': {'0': ['0', 1, 'q1'], '1': ['1', 1, 'q1'], '2': ['2', 1, 'q1'], ' ': [' ', -1, 'q2'], '+': ['+', 1, 'q1'], '-': ['-', 1, 'q1']},
    'q2': {'0': ['0', 0, 'qT'], '1': ['1', -1, 'q3'], '2': ['2', -1, 'q4']},
    'q3': {'+': ['+', -1, 'q5'], '-': ['-', -1, 'q6']},
    'q4': {'+': ['+', -1, 'q7'], '-': ['-', -1, 'q8']},
    'q5': {'0': ['1', 0, 'qT'], '1': ['2', 0, 'qT'], '2': ['0', -1, 'q5'], ' ': ['1', 0, 'qT']},
    'q6': {'0': ['2', -1, 'q6'], '1': ['0', -1, 'q9'], '2': ['1', 0, 'qT']},
    'q7': {'0': ['2', 0, 'qT'], '1': ['0', -1, 'q5'], '2': ['1', -1, 'q5']},
    'q8': {'0': ['1', -1, 'q6'], '1': ['2', -1, 'q6'], '2': ['0', 0, 'qT']},
    'q9': {'0': ['0', 0, 'qT'], '1': ['1', 0, 'qT'], '2': ['2', 0, 'qT'], ' ': [' ', 1, 'q10']},
    'q10': {'0': [' ', 1, 'q10'], '1': ['1', 0, 'qT'], '2': ['2', 0, 'qT'], ' ': ['0', 0, 'qT'], '-': ['-', -1, 'q10']}
         }

line = list(input())
q = 'q0'
curr = 0

while q!='qT':
    char, step, qnext = table[q][line[curr]]
    line[curr] = char
    curr += step
    q = qnext

print(''.join(line))
